void label_0481()
{
    dbmax = 0;
    dbsum = 0;

    for (const auto& info : item_generation_info_table)
    {
        if (info.level > objlv)
            continue;
        if (fltselect != info.fltselect)
            continue;
        if (flttypemajor != 0 && flttypemajor != info.category)
            continue;
        if (flttypeminor != 0 && flttypeminor != info.subcategory)
            continue;
        if (filtermax != 0)
        {
            // FIXME: use std::all_of
            bool ok = true;
            for (int i = 0; i < filtermax; ++i)
            {
                if (filter_item(info.id).find(filtern(i)) == std::string::npos)
                {
                    ok = false;
                    break;
                }
            }
            if (!ok)
                continue;
        }
        dbsum += info.rarity
                / (1000 + std::abs(info.level - objlv) * info.coefficient)
            + 1;
        dblist(0, dbmax) = info.id;
        dblist(1, dbmax) = dbsum;
        ++dbmax;
    }

    if (dbsum != 0)
    {
        // FIXME: do not use dbtmp
        exrand_rnd(dbtmp, dbsum);
        for (int i = 0; i < dbmax; ++i)
        {
            if (dblist(1, i) > dbtmp)
            {
                dbid = dblist(0, i);
                break;
            }
        }
    }
    else
    {
        dbid = 25;
    }
}


